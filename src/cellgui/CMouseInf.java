package cellgui;

public class CMouseInf {
    public int x;
    public int y;
    public int xMov;
    public int yMov;
    public boolean left;
    public boolean right;

    public CMouseInf() {
        this.x = 0;
        this.y = 0;
        this.xMov = 0;
        this.yMov = 0;
        this.left = false;
        this.right = false;
    }
}
